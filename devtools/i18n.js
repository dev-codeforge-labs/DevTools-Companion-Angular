  let currentLang = 'en';

  function t(key, vars) {
    const dict = TRANSLATIONS[currentLang] || TRANSLATIONS.en;
    let str = dict[key] ?? TRANSLATIONS.en[key] ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace('{' + k + '}', v);
      }
    }
    return str;
  }

  function getLang() { return currentLang; }

  function setLang(code) {
    if (!LANGS.includes(code)) return;
    currentLang = code;
    try { browser.storage.local.set({ ai_lang: code }); } catch (_) {}
    // Update tab button labels
    document.querySelectorAll('.tab-btn[data-tab]').forEach(btn => {
      const key = 'tab_' + btn.dataset.tab;
      const label = t(key);
      if (label !== key) btn.textContent = label;
    });
    // Sync the language dropdown
    const sel = document.getElementById('lang-selector');
    if (sel) sel.value = code;
    // Notify panel to re-render
    if (window._i18nLangChangeCallback) window._i18nLangChangeCallback(code);
  }

  function loadLang(cb) {
    try {
      browser.storage.local.get('ai_lang').then(result => {
        if (result.ai_lang && LANGS.includes(result.ai_lang)) {
          currentLang = result.ai_lang;
        }
        cb && cb(currentLang);
      }).catch(() => { cb && cb(currentLang); });
    } catch (_) {
      cb && cb(currentLang);
    }
  }

  window.t        = t;
  window.getLang  = getLang;
  window.setLang  = setLang;
  window.loadLang = loadLang;
